def get_system_prompt(mode: str, custom_prompt: str = "", facts: str = "") -> str:
    base = MODE_PROMPTS.get(mode, MODE_PROMPTS["default"])
    parts = [base]

    if facts:
        parts.append(f"\nФакты о пользователе:\n{facts}")

    if custom_prompt:
        parts.append(f"\nДополнительные инструкции от пользователя:\n{custom_prompt}")

    return "\n".join(parts)


